package com.spr;

public class Address {
	private String drno;
	private String city;
	private String state;
	public String getDrno() {
		return drno;
	}
	public String getCity() {
		return city;
	}
	public String getState() {
		return state;
	}
	public void setDrno(String drno) {
		this.drno = drno;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public void setState(String state) {
		this.state = state;
	}
	
	
}
